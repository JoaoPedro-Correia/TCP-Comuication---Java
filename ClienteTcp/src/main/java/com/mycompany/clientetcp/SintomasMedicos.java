    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<String> sintomasAtual = this.sintomas.getSelectedValuesList();
        String doencaAtual = (String)this.doencas.getSelectedItem();
        String result;

        // training
        if(treinamento.isSelected()){
            String msg = "1;"+client.getNomeDNS()+";"+sintomasAtual+";"+doencaAtual;

            try {
                result = this.client.enviarMensagem(msg);
                prompt.setText("Doença "+doencaAtual+" treinada:\n"+result);
            } catch (UnknownError | IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage()+". Erro no treinamento");
            }
        }else if(avaliacao.isSelected()){
            String msg = "2;"+client.getNomeDNS()+";"+sintomasAtual+";"+doencaAtual;
        
            try {
                result = this.client.enviarMensagem(msg);
                prompt.setText(result);
            } catch (UnknownError | IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage()+". Erro na avaliacao");
            }
        }else if(producao.isSelected()){
            String msg = "3;"+client.getNomeDNS()+";"+sintomasAtual;
        
            try {
                result = this.client.enviarMensagem(msg);
                prompt.setText(result);
            } catch (UnknownError | IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage()+". Erro na producao");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Voce precisa selecionar algum modo: TREINAMENTO, AVALIACAO, PRODUCAO");
        }
        
        System.out.println(sintomasAtual);
        System.out.println(doencaAtual);
    }//GEN-LAST:event_jButton1ActionPerformed

