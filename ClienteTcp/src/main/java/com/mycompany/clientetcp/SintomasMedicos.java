    public void setComboBox(JComboBox<String> comboBox, String[] itens) {
        comboBox.removeAllItems();

        for (String item : itens) {
            comboBox.addItem(item);
        }
    }
    
    //carregando sintomas
    private void loadSymptoms(){
        DefaultListModel model = new DefaultListModel();
        String msg = "4;"+client.getNomeDNS();
        String[] result;
        
        try {
            this.sintomas.setModel(model);
            result = this.client.enviarMensagem(msg).split(",");
            for(String str : result){model.addElement(str);}
        } catch (UnknownError | IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage()+". Não Inicializou os sintomas");
        }
    }
    
    //carregando doenças
    private void loadIllness(){
        String msg = "5;"+client.getNomeDNS();
        String[] result;
        
        try {
            result = this.client.enviarMensagem(msg).split(",");
            setComboBox(doencas, result);
        } catch (UnknownError | IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage()+". Não Inicializou as Doencas");
        }
    }

